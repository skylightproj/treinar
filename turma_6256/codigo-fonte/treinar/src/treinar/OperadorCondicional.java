package treinar;

public class OperadorCondicional {

	
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		boolean c = a > b && b > 10;
		System.out.println(c);
		
		boolean d = a > b || b > 10;
		System.out.println(d);
		
		boolean e = a > b & b > 10;
		System.out.println(e);
		
		boolean f = a > b | b > 10;
		System.out.println(f);
		
		
	}
	
	
}

package estudo.estatico;

public class ItemRefeicao {

	private String descricao;
	private Integer qtdCaloria;
	private Integer valorNutricional;
	
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public Integer getQtdCaloria() {
		return qtdCaloria;
	}
	public void setQtdCaloria(Integer qtdCaloria) {
		this.qtdCaloria = qtdCaloria;
	}
	public Integer getValorNutricional() {
		return valorNutricional;
	}
	public void setValorNutricional(Integer valorNutricional) {
		this.valorNutricional = valorNutricional;
	}
	
	
	
}
